package nl.Icaprojecten.ci.Spotitube.DTO;

import java.util.Date;

public class Track {
    private int idTrack;
    private String title;
    private String performer;
    private String album;
    private int playcount;
    private Date publicationDate;
    private Boolean offlineplay;
    private int lenght;

    public Track() {
    }

    public int getIdTrack() {
        return idTrack;
    }

    public void setIdTrack(int idTrack) {
        this.idTrack = idTrack;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPerformer() {
        return performer;
    }

    public void setPerformer(String performer) {
        this.performer = performer;
    }

    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public int getPlaycount() {
        return playcount;
    }

    public void setPlaycount(int playcount) {
        this.playcount = playcount;
    }

    public Date getPublicationDate() {
        return publicationDate;
    }

    public void setPublicationDate(Date publicationDate) {
        this.publicationDate = publicationDate;
    }

    public boolean getOfflineplay() {
        return offlineplay;
    }

    public void setOfflineplay(boolean offlineplay) {
        this.offlineplay = offlineplay;
    }

    public int getLenght() {
        return lenght;
    }

    public void setLenght(int lenght) {
        this.lenght = lenght;
    }
}
